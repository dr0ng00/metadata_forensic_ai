#!/usr/bin/env python3
"""
Create Results Directory Structure
"""

import os
from pathlib import Path

def create_directory_structure():
    """Create the complete results directory structure."""
    base_dir = Path("results")
    
    # Define directory structure
    directories = [
        base_dir,
        base_dir / "reports",
        base_dir / "reports" / "pdf",
        base_dir / "reports" / "json", 
        base_dir / "reports" / "html",
        base_dir / "exports",
        base_dir / "exports" / "csv",
        base_dir / "exports" / "xml",
    ]
    
    # Create directories
    for directory in directories:
        directory.mkdir(parents=True, exist_ok=True)
        print(f"Created: {directory}")
        
    # Create README files
    readme_files = {
        base_dir / "README.md": """# Results Directory

This directory contains all outputs generated by MetaForensicAI.

## Structure
- reports/: Analysis reports (PDF, JSON, HTML)
- exports/: Data exports (CSV, XML)

## Usage
View PDF reports: ls results/reports/pdf/*.pdf
Process JSON data: jq '.risk_assessment.score' results/reports/json/*.json
Export to CSV: python scripts/export_to_csv.py
""",
        base_dir / "reports" / "README.md": """# Reports Directory

Contains forensic analysis reports in multiple formats:
- PDF: Full analysis reports with visualizations
- JSON: Structured data for programmatic access
- HTML: Interactive reports for web viewing
""",
        base_dir / "exports" / "README.md": """# Exports Directory

Contains exported data in various formats:
- CSV: Comma-separated values for spreadsheet analysis
- XML: Structured XML format for data interchange
"""
    }
    
    # Write README files
    for readme_path, content in readme_files.items():
        readme_path.write_text(content)
        print(f"Created: {readme_path}")
    
    print(f"\nResults directory structure created at: {base_dir.absolute()}")
    return base_dir

if __name__ == "__main__":
    create_directory_structure()
